package exercicios.poo.ex22figurasgeometricasheranca;

public abstract class FiguraGeometrica {
    String nome;
    String cor;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }
}
